        /// <summary>
        /// 获得数据列表
        /// </summary>
        public List<ATOM.Model.City> DataTableToList(DataTable dt)
        {
            List<ATOM.Model.City> modelList = new List<ATOM.Model.City>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                ATOM.Model.City model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = dal.DataRowToModel(dt.Rows[n]);
                    if (model != null)
                    {
                        modelList.Add(model);
                    }
                }
            }
            return modelList;
        }

